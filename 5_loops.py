# the keyword use is "for"
my_fruits = ["mango", "orange", "peach", "apple"]

for fruit in my_fruits:  # fruit is an itterator created automatically by python
    print(fruit)
# len is a function to get the length
print(f"the list{my_fruits} has {len(my_fruits)} items")

my_name = "jonathan"
for i in my_name:
    print(i)
